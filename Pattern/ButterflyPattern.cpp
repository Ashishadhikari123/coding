#include<iostream>
using namespace std;

void pattern(int n)
{
	int spaces;
	for(int i=1;i<=n;i++)
	{
		for(int j=1;j<=i;j++)
		{
			cout<<"*";
		}
		spaces=(2*n)-(2*i);
		for(int j=1;j<=spaces;j++)
		{
			cout<<" ";
		}
		for(int j=1;j<=i;j++)
		{
			cout<<"*";
		}
		cout<<endl;
	}
	for(int i=n;i>=1;i--)
	{
		for(int j=1;j<=i;j++)
		{
			cout<<"*";
		}
		spaces=(2*n)-(2*i);
		for(int j=1;j<=spaces;j++)
		{
			cout<<" ";
		}
		for(int j=1;j<=i;j++)
		{
			cout<<"*";
		}
		cout<<endl;
	}
}

int main()
{
	int n;
	cout<<"Enter the value of n"<<endl;
	cin>>n;
	pattern(n);
	return 0;
}